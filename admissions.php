                    <h3>Driving Licence</h3>
                    <div class="first">
						 <p>The person who wants a driving license is required to apply in the prescribed Form. The applicant is required to pass computer as well as driving test in order to get a license.</p>
					</div>
                    <div>
                        <h2>Application Form</h2>
                        <ul>
							<li><p>An applicant is required to obtain a learning license in order to get new driving license.</p>
							<li><p>An applicant is required to apply in Form No.2/4 for the purpose of both learning as well as driving license, Medical Certificate in Form No.1 (A) is also required to be attached.</p>
						</ul>
					</div>
                    <div>
                        <h2>Eligibility</h2>
                        <ul>
							<li>A person should have completed 16 years to obtain license for 2 wheelers without gear. 
							<li>A person should have completed 18 years age to obtain license for 2 wheelers with gear, Motor-car and other non-transport vehicles.
							<li>For transport vehicles, a person should have completed 20 years of age. In addition he should be passed standard 8th pass and should have experience of 1 year driving a light motor vehicle.
						</ul>
                    </div>
					<div>
                        <h2>Necessary Documents</h2>
                        <ul>
							<li><b>Proof of Age</b>: School Leaving Certificate, Birth Certificate, Passport, Pan Card, L.I.C. Policy or a Certificate from Civil Surgeon or a Doctor equivalent to that can be submitted as a proof of age.
							<li><b>Proof of Address</b>: School Leaving Certificate, Passport, L.I.C. Policy, Electoral Voter I-Card, Light Bill, Telephone Bill, receipt of House Tax with address, Pay Slip of Central/State/Local self Government or an Affidavit from the applicant to that effect can be submitted asthe proof for address. 
						</ul>
                    </div>
					<div>
                        <h2>Fee</h2>
                        <ul>
							<li>The fee for Learning License and Driving License has to be paid together.Rs.25/-as test fee plus Rs.30/- per category of vehicle is required to be paid for Learning License.
							<li>Rs.200/- for a Smart Card Driving License and Rs.50/- per category of vehicle is required to be paid for Driving License.						
						</ul>
